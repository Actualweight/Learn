package Api;

public class API {
    public static void main(String[] args) {
           
            System.out.println("3的10次方是："+Math.pow(3,10));
            System.out.println("27的立方根是："+Math.cbrt(27));
            System.out.println("256的平方根是："+Math.sqrt(256));
            System.out.println("-5的绝对值是："+Math.abs(-5));
            System.out.println("随机数是:"+Math.random());
            System.out.println("大的数是："+Math.max(8,10));
            System.out.println("小的数是："+Math.min(11,5));
            System.out.println("4.2四舍五入是："+Math.round(4.2));
            System.out.println("11.1向上取整后是："+Math.ceil(11.1));
            System.out.println("11.9向下取整后是："+Math.floor(11.9));
    }
}                           
